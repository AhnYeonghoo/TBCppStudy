#include <iostream>

using namespace std;

int main()
{
	int x = 2;
	cout << 1 + 2 << endl;
	cout << x + 2 << endl;
	cout << x * 2 << endl;
	cout << -x << endl;
	cout << x / 2 << endl;
	cout << x % 2 << endl;

	int y = (x > 0) ? 1 : 2;
	cout << y << endl;
	cout << "Hello, World!" << endl;
}